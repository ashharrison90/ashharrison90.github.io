import Head from 'next/head'
import Link from 'next/link'
import dynamic from 'next/dynamic'
import { getAllPosts } from '../lib/api'
import Layout from '../components/Layout/Layout'
import styles from '../styles/Home.module.scss'

const ThemeToggle = dynamic(() => import('../components/ThemeToggle/ThemeToggle'), {
  ssr: false,
})

export interface Props {
  allPosts: {
    title: string,
    date: string,
    author: {
      name: string,
      image: string
    },
    slug: string,
    coverImage: string
    excerpt: string
  }[]
  buildTimestamp: number
}

export default function Home({ allPosts, buildTimestamp }: Props) {
  return (
    <Layout buildTimestamp={buildTimestamp}>
      <Head>
        <title>Create Next App</title>
        <meta name='description' content='Generated by create next app' />
        <link rel='icon' href='/favicon.ico' />
      </Head>

      <main className={styles.main}>
        <h1 className={styles.title}>
          Welcome to <a href='https://nextjs.org'>Next.js!</a>
        </h1>

        {
          allPosts.map(post => (
            <Link key={post.slug} as={`/posts/${post.slug}`} href="/posts/[slug]">
              <a className={styles.card}>
                <p>{post.title}</p>
                <p>{post.date}</p>
                <p>{post.author.name}</p>
                <p>{post.slug}</p>
                <p>{post.coverImage}</p>
                <p>{post.excerpt}</p>
              </a>
            </Link>
          ))
        }

        <ThemeToggle />
      </main>
    </Layout>
  )
}

export const getStaticProps = () => {
  const allPosts = getAllPosts([
    'title',
    'date',
    'slug',
    'author',
    'coverImage',
    'excerpt',
  ])
  
  return {
    props: {
      allPosts,
      buildTimestamp: Date.now()
    }
  }
}
