import Head from 'next/head'
import { getAllPosts } from '../lib/postsApi'
import PostGrid from '../components/PostGrid/PostGrid'
import PostCard from '../components/PostCard/PostCard'
import Layout from '../components/Layout/Layout'
import LinkButton from '../components/LinkButton/LinkButton'
import styles from '../styles/Home.module.scss'

export interface Props {
  allPosts: {
    title: string,
    date: string,
    author: {
      name: string,
      image: string
    },
    slug: string,
    coverImage: string
    excerpt: string
  }[]
}

export default function Home({ allPosts }: Props) {
  return (
    <Layout showHero>
      <Head>
        <title>hi, i'm ash</title>
        <meta name='description' content='Generated by create next app' />
        <link rel='icon' href='/favicon.ico' />
      </Head>

      <main>
        <div className={styles.about}>
          <h2>about</h2>
          <LinkButton className={styles.aboutButton} href='/about'>About</LinkButton>
        </div>

        <div className={styles.posts}>
          <h2>posts</h2>
          <PostGrid>
            {
              allPosts.map(post => (
                <PostCard
                  key={post.slug}
                  coverImage={post.coverImage}
                  date={post.date}
                  excerpt={post.excerpt}
                  slug={post.slug}
                  title={post.title}
                />
              ))
            }
          </PostGrid>
          <LinkButton className={styles.postsButton} href='/posts'>View all posts</LinkButton>
        </div>
      </main>
    </Layout>
  )
}

export const getStaticProps = () => {
  const allPosts = getAllPosts()
  
  return {
    props: {
      allPosts
    }
  }
}
